import 'package:flutter/material.dart';
import 'prefs_service.dart';

enum AppLanguage { english, hindi, marathi, tamil, telugu, kannada }

class LocaleProvider extends ChangeNotifier {
  AppLanguage _language = AppLanguage.english;

  LocaleProvider() {
    final saved = PrefsService.languageCode;
    _language = AppLanguage.values.firstWhere(
      (l) => l.name == saved,
      orElse: () => AppLanguage.english,
    );
  }

  AppLanguage get language => _language;

  void setLanguage(AppLanguage lang) {
    _language = lang;
    PrefsService.languageCode = lang.name;
    notifyListeners();
  }

  String get(String key) => _strings[_language]?[key] ?? _strings[AppLanguage.english]![key] ?? key;
}

String tr(BuildContext context, String key) {
  // Simple function - but we'll use provider
  return key;
}

